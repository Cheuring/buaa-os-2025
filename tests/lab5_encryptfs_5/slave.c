char buf[512];

void write_msg(char *msg_str, char *test_name, int offset) {
	int r;
	int msg_fd;

	if ((r = open("/saved_msg", O_RDWR | O_ENCRYPT)) < 0) {
		user_panic("[%s] cannot open /saved_msg: %d", test_name, r);
	}
	msg_fd = r;
	memcpy(buf, msg_str, strlen(msg_str) + 1);
	if ((r = seek(msg_fd, offset)) < 0) {
		user_panic("[%s] cannot seek /saved_msg: %d", test_name, r);
	}
	debugf("\n[%s] seek /saved_msg passed, now at %d\n", test_name, offset);
	if ((r = write(msg_fd, buf, strlen(msg_str) + 1)) < 0) {
		user_panic("[%s] cannot write /saved_msg: %d", test_name, r);
	}
	if ((r = close(msg_fd)) < 0) {
		user_panic("[%s] cannot close /saved_msg: %d", test_name, r);
	}
	debugf("\n[%s] write /saved_msg at %d passed\n", test_name, offset);
}

void compare_msg(char *msg_str, char *encrypt_array, char *test_name, int offset) {
	int r;
	int msg_fd;

	if ((r = open("/saved_msg", O_RDONLY)) < 0) {
		user_panic("[%s] cannot open /saved_msg: %d", test_name, r);
	}
	msg_fd = r;
	if ((r = seek(msg_fd, offset)) < 0) {
		user_panic("[%s] cannot seek /saved_msg: %d", test_name, r);
	}
	debugf("\n[%s] seek /saved_msg passed, now at %d\n", test_name, offset);
	if ((r = read(msg_fd, buf, sizeof(buf))) < 0) {
		user_panic("[%s] cannot read /saved_msg: %d", test_name, r);
	}
	for (int i = 0; i < strlen(msg_str) + 1; i++) {
		if (buf[i] != encrypt_array[i]) {
			user_panic("[%s] read /saved_msg returned wrong data at %d: %02x != %02x",
				   test_name, offset + i, (unsigned char)buf[i],
				   (unsigned char)encrypt_array[i]);
		}
	}
	if ((r = close(msg_fd)) < 0) {
		user_panic("[%s] cannot close /saved_msg: %d", test_name, r);
	}
	debugf("\n[%s] read /saved_msg at %d passed\n", test_name, offset);
}

void set_key(char *key_path, char *test_name) {
	int r;
	int key_fd;

	if ((r = open(key_path, O_RDONLY)) < 0) {
		user_panic("[%s] cannot open %s: %d", test_name, key_path, r);
	}
	key_fd = r;
	if ((r = fskey_set(key_fd)) != 0) {
		user_panic("[%s] fskey_set() failed: %d", test_name, r);
	}
	if ((r = fskey_isset()) != 1) {
		user_panic("[%s] fskey_isset() failed: %d", test_name, r);
	}
	if ((r = close(key_fd)) < 0) {
		user_panic("[%s] cannot close %s: %d", test_name, key_path, r);
	}
	debugf("\n[%s] fskey_set(%s) passed\n", test_name, key_path);
}

